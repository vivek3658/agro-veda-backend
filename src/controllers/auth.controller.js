const authService = require('../services/auth.service');

const register = async (req, res) => {
  try {
    const { name, email, password, reEnterPassword, role, captcha } = req.body;
    
    // Basic captcha validation check
    if (!captcha) {
      return res.status(400).json({ message: 'Captcha is required' });
    }
    
    // Verify passwords match
    if (password !== reEnterPassword) {
      return res.status(400).json({ message: 'Passwords do not match' });
    }

    const result = await authService.registerUser({ name, email, password, role });
    res.status(201).json(result);
  } catch (error) {
    res.status(400).json({ message: error.message });
  }
};

const login = async (req, res) => {
  try {
    const { email, password, captcha } = req.body;

    // Basic captcha validation check
    if (!captcha) {
      return res.status(400).json({ message: 'Captcha is required' });
    }

    const result = await authService.loginUser(email, password);
    res.status(200).json(result);
  } catch (error) {
    res.status(401).json({ message: error.message });
  }
};

const googleLogin = async (req, res) => {
  try {
    // Expect an ID token generated by Google Client on frontend
    const { idToken, role } = req.body; 
    
    if (!idToken) {
        return res.status(400).json({ message: 'Google ID token is required' });
    }

    const result = await authService.googleLogin(idToken, role);
    res.status(200).json(result);
  } catch (error) {
    res.status(401).json({ message: error.message });
  }
};

// Returns current authenticated user via middleware
const getMe = async (req, res) => {
  res.status(200).json(req.user);
};

module.exports = { register, login, googleLogin, getMe };
